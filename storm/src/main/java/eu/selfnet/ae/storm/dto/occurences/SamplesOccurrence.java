package eu.selfnet.ae.storm.dto.occurences;

public class SamplesOccurrence {
    private String numberOfOccurrences;
    private String numberOfSamples;

    public SamplesOccurrence() {
    }

    public String getNumberOfOccurrences() {
        return numberOfOccurrences;
    }

    public void setNumberOfOccurrences(String numberOfOccurrences) {
        this.numberOfOccurrences = numberOfOccurrences;
    }

    public String getNumberOfSamples() {
        return numberOfSamples;
    }

    public void setNumberOfSamples(String numberOfSamples) {
        this.numberOfSamples = numberOfSamples;
    }

    
}
